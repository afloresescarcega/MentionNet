def bfs(feed):
	q = []
	q.append(feed["data"])		
	while q:
		current = q.pop(0)
		for feed in current:
			pp(feed["from"]["name"])
			if "comments" in feed:
				#Node existed, enqueue it
				q.append(feed["comments"]["data"])

def dfs_recursive(feed):
	for feed in feed["data"]:
		pp(feed["from"]["name"])
		if "comments" in feed:
			dfs_recursive(feed["comments"])

